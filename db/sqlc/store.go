package db

import (
	"context"
	"database/sql"
	"fmt"
)

// Store provides all functions to executedb queries and transcations
type Store struct {
	*Queries         //this is called composition , embedding queries
	db       *sql.DB //required to create new db transaction
}

// Creates a NewStore
func NewStore(db *sql.DB) *Store {
	return &Store{
		db:      db,
		Queries: New(db), //generated by sqlc
	}
}

// execTx executes a function within a database transaction
func (store *Store) execTx(ctx context.Context, fn func(*Queries) error) error {
	tx, err := store.db.BeginTx(ctx, nil)
	if err != nil {
		return err
	}
	q := New(tx)
	err = fn(q)
	// if there is error than we will rollback
	if err != nil {
		if rbErr := tx.Rollback(); rbErr != nil {
			return fmt.Errorf("tx err : %v, rb err : %v", err, rbErr) //it returns rollback err and err
		}
		return err //if no error in rollback than trasaction error
	}
	return tx.Commit()
}

// contains the input parameter of the transfer transaction
type TransferTxParams struct {
	FromAccountID int64 `json:"from_account_id"`
	ToAccountID   int64 `json:"to_account_id"`
	Amount        int64 `json:"amount"`
}

// TransferTxResult is the result of the transfer trncation
type TransferTxResult struct {
	Transfer    Transfer `json:"transfer"`
	FromAccount Account  `json:"from_account"`
	ToAccount   Account  `json:"to_account"`
	FromEntry   Entry    `json:"from_entry"`
	ToEntry     Entry    `json:"to_entry"`
}

